package com.tcc.pojo;

public class StTable {
    private Integer stId;

    private Integer stSf;

    private Integer stBt;

    private Integer stYear;

    public Integer getStId() {
        return stId;
    }

    public void setStId(Integer stId) {
        this.stId = stId;
    }

    public Integer getStSf() {
        return stSf;
    }

    public void setStSf(Integer stSf) {
        this.stSf = stSf;
    }

    public Integer getStBt() {
        return stBt;
    }

    public void setStBt(Integer stBt) {
        this.stBt = stBt;
    }

    public Integer getStYear() {
        return stYear;
    }

    public void setStYear(Integer stYear) {
        this.stYear = stYear;
    }
}